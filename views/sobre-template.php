<h1>Sobre a Cooperativa de reciclagem União</h1>
<p>O sistema baseia­se no gerenciamento de uma cooperativa de reciclagem, em que será
mantido o controle de todos os colaboradores e com qual função ele colaborou em
determinado dia para o ciclo de reciclagem, onde podem ser separados na parte da coleta,
separação dos materiais, embalagem e transporte para a empresa. Também será controlado
informações sobre o material coletado e reciclado, bem como a sua quantidade e o destino do
mesmo. O sistema tem como usuários básicos o administrador da cooperativa, seus
colaboradores ou apenas os que tiverem maior participação, as empresas que transformam o
material em novos produtos, os doadores, pode ser considerado também toda a população,
    que será incluída no parte da transparência.</p>
<p>
O principal operador do sistema é o administrador da cooperativa, será guardado seu
nome, CPF, endereço e telefone, apenas para conferências futuras, caso seja necessário.
O trabalho diário será registrado pelo administrador, funcionário que tem acesso total
à todas as funções do sistema, ele fará isso sempre ao final do dia após analisar os dados
diários, toda a contabilidade da cooperativa será feita por ele.</p>
<p>
Sobre os colaboradores será armazenado seu nome, CPF, endereço e telefone para
contato, eles poderão fazer observações ao longo do dia de coleta e seletiva, isso inclui:
informações de como está ocorrendo o trabalho na cooperativa caso o administrador esteja
ausente no momento, informações de onde há material para ser coletado, afim de enviar uma
equipe para esse local, qual função esse colaborador executou nesse dia, que também
funcionará como uma espécie de ponto diário para registrar a presença do funcionário na
cooperativa,</p>
    <p>
Já as empresas que receberão esse material, já separado pela cooperativa, terão seu
CNPJ e a localização inicialmente cadastrados no sistema para manter o controle, elas
poderão ter acesso ao sistema para verificar os dados sobre a transição dos materiais.
Os doadores poderão ser anônimos ou ter suas informações básicas como as dos
demais atores do sistema cadastradas no mesmo, eles poderão contribuir financeiramente ou
com doações de maquinário para a cooperativa.</p>
<p>
O sistema da cooperativa será baseado na transparência pública, ou seja, o público em
geral poderá acessar o sistema de modo “restrito” e ficar por dentro de todas as informaçõesrelacionadas à cooperativa, divulgando assim os dados e também podendo colaborar com a
conscientização da população para a importância da reciclagem nos dias atuais.</p>